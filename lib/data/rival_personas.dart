enum RivalStatus {
  idle,
  listing,
  highBid,
  sweating,
  tappedOut,
}

extension RivalStatusX on RivalStatus {
  String get label => switch (this) {
        RivalStatus.idle => 'browsing',
        RivalStatus.listing => 'listing',
        RivalStatus.highBid => 'high bid',
        RivalStatus.sweating => 'sweating',
        RivalStatus.tappedOut => 'tapped-out',
      };
}

class RivalPersona {
  const RivalPersona({
    required this.id,
    required this.handle,
    required this.title,
    required this.personality,
    required this.quirk,
    required this.fairBias,
    required this.seed,
    required this.avatarAsset,
    this.wealthMult = 1.0,
    this.aggression = 0.4,
  });

  final String id;
  final String handle;
  final String title;
  final List<String> personality;
  final String quirk;
  /// Offer bias: <1 cheapskate, >1 generous / overpays.
  final double fairBias;
  final int seed;
  final String avatarAsset;
  /// Portfolio / cash stack vs player (whales > 1).
  final double wealthMult;
  /// Pit / board aggression 0–1 (snipes, overbids, hype).
  final double aggression;
}

